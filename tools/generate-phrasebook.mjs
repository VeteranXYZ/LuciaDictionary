function slugify(text) {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function replyFor(speaker, intent) {
  if (speaker === "classmate") return [{ en: "Sure.", cn: "可以。" }];
  if (speaker === "student" || intent === "ask_help" || intent === "stay_safe")
    return [{ en: "I need help, please.", cn: "请帮帮我。" }];
  if (speaker === "school")
    return [{ en: "I will show my parent.", cn: "我会给家长看。" }];
  return [{ en: "Okay, I will do it.", cn: "好的，我会做。" }];
}

